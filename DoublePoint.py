import Point

class DoublePoint:

    def __init__(self, point1, point2):
        self.point1 = point1
        self.point2 = point2

    def getPointX1(self):
        return self.point1.x

    def getPointX2(self):
        return self.point2.x

    def getPointY1(self):
        return self.point1.y

    def getPointY2(self):
        return self.point2.y

